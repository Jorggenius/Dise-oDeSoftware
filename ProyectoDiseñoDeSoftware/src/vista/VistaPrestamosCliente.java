       private void agregarListenerTabla() {
        ListSelectionModel model = tablaLibros.getSelectionModel();
        model.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()) {
                    int selectedRow = tablaLibros.getSelectedRow();
                    if (selectedRow != -1) {
                        // Obtén el valor de la columna "№ Plaza"
                        String placa =  (String) tablaLibros.getValueAt(selectedRow, 2);
                        txtIsbn.setText(placa);
                    }
                }
            }
        });
    }
