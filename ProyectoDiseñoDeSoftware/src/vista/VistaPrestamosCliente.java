    private void llenarTabla() {
        DefaultTableModel model = new DefaultTableModel(new String[]{"Nombre", "Autor", "Isbn", "Editorial", "Npag",
            "Disponibilidad", "genero"},
                libros.size());
        tablaLibros.setModel(model);
        TableModel modelP = tablaLibros.getModel();
        for (int i = 0; i < libros.size(); i++) {
            Libro libro = libros.get(i);
            modelP.setValueAt(libro.getNombre(), i, 0);
            modelP.setValueAt(libro.getAutor(), i, 1);
            modelP.setValueAt(libro.getIsbn(), i, 2);
            modelP.setValueAt(libro.getEditorial(), i, 3);
            modelP.setValueAt(libro.getNpaginas(), i, 4);
            modelP.setValueAt(libro.getDisponibilidad(), i, 5);
            modelP.setValueAt(libro.getGenero(), i, 6);
        }
    }
